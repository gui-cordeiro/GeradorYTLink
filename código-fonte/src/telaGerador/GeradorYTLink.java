    public int gerarLink(String url, String imgTitle, String videoAlt){
        char link[] = url.toCharArray();
        char[] converter = new char[11];
        
        //Considerando um link inválido
        if(!url.contains("youtu.be") && !url.contains("youtube.com")){
            JOptionPane.showMessageDialog(null, "URL inválida. Verifique a URL e tente novamente (URL oriunda de uma playlist não funciona).", "ERRO", JOptionPane.ERROR_MESSAGE);
            return 0;
        //Considerando o link encurtado do YouTube
        }else if(url.contains("youtu.be")){
            for(int c = 0; c < 11; c ++){
                converter[c] = link[17 + c];
            }
        //Considerando o link padrão do YouTube
        }else if(url.contains("youtube.com")){
            for(int c = 0; c < 11; c ++){
                converter[c] = link[32 + c];
            }
        }
        String IDVideo = new String(converter);
        txtResultado.setText("[![" + imgTitle + "](https://img.youtube.com/vi/" + IDVideo + "/0.jpg)](" + url + " \"" + videoAlt + "\")");
        panelResultado.setVisible(true);
        btnGeradorLink.setIcon(new ImageIcon(getClass().getResource("/imagens/btnClean.png")));
        return 0;
    }
    

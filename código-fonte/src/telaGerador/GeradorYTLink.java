    private void btnGeradorLinkMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnGeradorLinkMouseClicked
        if(btnGeradorLink.getIcon().toString().contains("/imagens/btnLink.png")){
            String url = txtURLVideo.getText();
            String imgTitle = txtTituloVideo.getText();
            String videoAlt = txtAcessi.getText();
            //Campo da URL inválido
            if(url.equals("Cole aqui a URL do vídeo") || url.equals("")){
                JOptionPane.showMessageDialog(null, "O campo \"" + lblURLVideo.getText().replace(":", "") + "\" está vazia.", "ATENÇÃO", JOptionPane.WARNING_MESSAGE);
                //Campo do título inválido
            }else if(imgTitle.equals("Insira o título do vídeo") || imgTitle.equals("")){
                JOptionPane.showMessageDialog(null, "O campo \"" + lblTitleVideo.getText().replace(":", "") + "\" está vazia.", "ATENÇÃO", JOptionPane.WARNING_MESSAGE);
                //Campo da descrição de acessibilidade inválido
            }else if(videoAlt.equals("Insira o título de acessiblilidade") || videoAlt.equals("")){
                Object[] opt = {"Sim", "Não"};
                int resp = JOptionPane.showOptionDialog(null, "O campo \"" + lblTitleAcess.getText().replace(":", "") + 
                        "\" está vazia (campo não obrigatório). Quer continuar mesmo assim?", "IMPORTANTE",
                        JOptionPane.YES_NO_CANCEL_OPTION, JOptionPane.QUESTION_MESSAGE, null, opt, opt[1]);
                if(resp == 0){ //SIM
                    videoAlt = "Clique aqui para ver o vídeo";
                    gerarLink(url, imgTitle, videoAlt);
                }       
            }else{
                gerarLink(url, imgTitle, videoAlt);
            }
        }else if(btnGeradorLink.getIcon().toString().contains("/imagens/btnClean.png")){
            txtURLVideo.setText("Cole aqui a URL do vídeo");
            txtTituloVideo.setText("Insira o título do vídeo");
            txtAcessi.setText("Insira o título de acessiblilidade");
            lblLinkGerado.setText("Link Gerado!");
            txtResultado.setText("");
            panelResultado.setVisible(false);
            Tela1.requestFocusInWindow();
            btnGeradorLink.setIcon(new ImageIcon(getClass().getResource("/imagens/btnLink.png")));
        }
        
    }//GEN-LAST:event_btnGeradorLinkMouseClicked

